public class OperTest1 
{
	public static void main(String[] args) 
	{
		// 증감 연산자
		int x = 10;
		System.out.println(x);
		System.out.println(x++ + "     "  + x);
		System.out.println(--x + "     "  + x);
		System.out.println(x++ + "     "  + x);
		System.out.println(++x + "     "  + x);
		System.out.println(x-- + "     "  + x);
		System.out.println(--x + "     "  + x);
		System.out.println(x++ + "     "  + x);
		System.out.println(x);

	}
}
